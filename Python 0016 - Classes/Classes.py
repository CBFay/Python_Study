class arithmetic:

    def add(x,y):
        sum = x + y
        print(sum)

    def sub(x,y):
        dividend = x - y
        print(dividend)

    def mult(x,y):
        product = x * y
        print(product)

    def div(x,y):
        quotient = x / y
        print(quotient)

arithmetic.multiplication(3,5)
